     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Models\Category  $category
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        //
        $category = Category::findOrFail($id);
        $category->update([
            'name' => $request->name,
        ]);
        return response()->json(['message' => 'Category update successfully'], 200);
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Models\Category  $category
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $category = Category::findOrFail($id);

        // Kiểm tra xem danh mục có sản phẩm nào không
        $hasProducts = Product::where('category_id', $id)->exists();

        if ($hasProducts) {
            return response()->json(['message' => 'Category cannot be deleted because it has products'], 400);
        }

        // Nếu không có sản phẩm nào, xóa danh mục
        $category->delete();
        return response()->json(['message' => 'Category deleted successfully'], 200);
    }
